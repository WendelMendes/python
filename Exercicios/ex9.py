#Escreva um programa que leia um valor em metros e o exiba convertido em centímetros e milímetros.
valor=float(input("Digite um valor em metros: "))
print("A quantidade em metros é {}.".format(valor))
print("A quantidade em centimetros é {}.".format(valor*100))
print("A quantidade em milímetros é {}.".format(valor*1000))
